        <h1>Financial Literacy at the Haven</h1>
        <h2>Mission:</h2>
        <p class="sarah-p">The Financial Aid Office at Lock Haven University provides a multi-faceted financial literacy program to ensure students develop an understanding of the importance of budgeting, savings and debt. Lock Haven University currently partners with and implements tools provided by agencies like: Department of Education, PHEAA, PSECU, PASFAA and local high schools. We strive to reach students by working with out Institutional Research Office and displaying clear readable communication to our audience. Ensuring that we effectively communicate the importance of repaying student loans, remains one of our highest priorities. Our staff members always stress that students show borrow conservatively. Our hope is that we are able to reduce financial aid stress and improve student engagement by providing holistic financial aid advice, which aims to demystify and simplify what can sometimes be considered an overwhelming process for our students, parents and community.</p>

        <h1>How do we achieve our mission?</h1>
        <div class = "info-list">
