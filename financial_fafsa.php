        <h1>How Students File for FAFSA</h1>
        <ul style="list-style-type:disc">
            <li class = "sarah-li">Students who would like to be reviewed for financial aid eligibility must file based on their tax return from two years earlier. This year is often referred to as the “base year” or the “prior-prior year.”</li>
            <li class = "sarah-li">Students who are eligible for the Pennsylvania State Grant will receive an email from PHEAA. In the email they will be asked to complete the State Grant Form. First time recipients must complete a form or PHEAA will not forward the funds to LHU. To complete the form students can go to www.pheaa.org and log in through “account access”.</li>
            <li class = "sarah-li">To file a FAFSA online, go to <strong><a href="https://studentaid.ed.gov/sa/fafsa">https://studentaid.ed.gov/sa/fafsa</a></strong></li>
            <li class = "sarah-li">LHU’s school code is 003323</li>
            <li class = "sarah-li">Students and parents are strongly urged to utilize the IRS Data Retrieval Tool when filing (Link to IRS Data Retrieval Tool: <a href="https://www.lockhaven.edu/financialaid/overview/#tab-3">https://www.lockhaven.edu/financialaid/overview/#tab-3</a>Forgot your ID? View <a href="https://www.youtube.com/watch?time_continue=5&v=bZkF4ZoZwzc" target="_blank" rel="noopener">this video</a> to learn how to retrieve your ID.</li>
            <li class = "sarah-li">Filing early is crucial because funds may be limited. If you miss the deadline, you should complete the FAFSA as soon as possible. Pennsylvania undergraduate students should note the FAFSA deadline for the Pennsylvania State Grant is May 1st.</li>
            <li class = "sarah-li">The Pennsylvania State Grant or PHEAA State Grant has a deadline of May 1. All students who submit a FAFSA after that date will automatically be ineligible even if they qualify for the grant.</li>
            <li class = "sarah-li">Students who complete the FAFSA will be considered for the Federal Pell Grant, the Federal Supplemental Educational Opportunity Grant (SEOG), TEACH, the Pennsylvania State Grant (or other state grant), the Federal Perkins Loan, Federal Direct Stafford Loan, as well as the Federal Work Study Program.</li>
            <li class = "sarah-li">Applying for financial aid is an annual process. You must reapply for financial aid every year during your college career.</li>
        </ul>
        
